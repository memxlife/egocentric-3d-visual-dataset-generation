#!/usr/bin/env python3
from __future__ import annotations

import argparse
import re
from pathlib import Path


LINK_RE = re.compile(r"(?<!!)\[([^\]]+)\]\(([^)]+)\)")


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Build a Codex-friendly Markdown file with absolute local links."
    )
    parser.add_argument("--input", type=Path, default=Path("docs/spec/README.md"))
    parser.add_argument("--output", type=Path, default=Path("docs/spec/README.codex.md"))
    args = parser.parse_args()

    source = args.input.resolve()
    output = args.output.resolve()
    text = source.read_text()

    def replace(match: re.Match[str]) -> str:
        label, target = match.group(1), match.group(2)
        if _should_keep_target(target):
            return match.group(0)
        absolute = (source.parent / target).resolve()
        return f"[{label}](<{absolute}>)"

    converted = LINK_RE.sub(replace, text)
    banner = (
        "<!-- This file is generated by scripts/build_codex_links.py. "
        "Edit docs/spec/README.md instead. -->\n\n"
    )
    output.write_text(banner + converted)
    print(output)


def _should_keep_target(target: str) -> bool:
    return (
        "://" in target
        or target.startswith("#")
        or target.startswith("/")
        or target.startswith("mailto:")
    )


if __name__ == "__main__":
    main()
